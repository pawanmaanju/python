# Example string with various escape sequences
example_string = "Hello,\nWelcome to the \"Escape Sequences\" tutorial.\n"

# Newline
newline_example = "This is line one.\nThis is line two."

# Tab
tab_example = "Column1\tColumn2\tColumn3"

# Backslash
backslash_example = "This is a backslash: \\"

# Single quote
single_quote_example = 'It\'s a nice day.'

# Double quote
double_quote_example = "She said, \"Hello!\""

# Carriage return
carriage_return_example = "Hello, World!\r12345"

# Backspace
backspace_example = "Hello, World!\b\b\b123"

# Form feed
form_feed_example = "Page 1\fPage 2"

# Unicode character (Greek letter Omega)
unicode_example = "Unicode character: \u03A9"

# Octal value (ASCII code for 'A')
octal_example = "Octal value: \101"

# Hexadecimal value (ASCII code for 'A')
hex_example = "Hexadecimal value: \x41"

