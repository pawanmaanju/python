# Taking first input from the user and converting it to an integer
a = int(input("Enter first Number: "))

# Taking second input from the user and converting it to an integer
b = int(input("Enter second Number: "))

# Performing addition of the two numbers
c = a + b
# Displaying the result of addition
print("Addition Is: ", c)

# Performing subtraction of the two numbers
c = a - b
# Displaying the result of subtraction
print("Subtraction Is: ", c)

# Performing multiplication of the two numbers
c = a * b
# Displaying the result of multiplication
print("Multiplication Is: ", c)

# Performing division of the two numbers
c = a / b
# Displaying the result of division
print("Division Is: ", c)