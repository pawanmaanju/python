list1 = ["Apple", "Mango", "Cat", 20, 11, 9.88, 89.99, True, False, "Rakesh", "Mahesh"]

# Print the original list
print(list1)

# Append "Pankaj" to the list
list1.append("Pankaj")

# Print the list after appending
print(list1)

# Reverse the list
list1.reverse()

# Print the list after reversing
print(list1)

list2 = [88, 33, 66, 99, 11, 77, 44, 55]

# Print the original list2
print(list2)

# Sort list2 in ascending order
list2.sort()

# Print list2 after sorting
print(list2)

# Insert 45 at index 2
list2.insert(2, 45)

# Print list2 after inserting 45
print(list2)

# Remove the element at index 2
list2.pop(2)

# Print list2 after popping the element at index 2
print(list2)

# Remove the first occurrence of 44
list2.remove(44)

# Print list2 after removing 44
print(list2)